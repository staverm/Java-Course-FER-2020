package hr.fer.zemris.java.hw06.shell;

/**
 * Enum that represents the current Shell status. If the Shell status is
 * TERMINATE, the program should end. If the Shell status is CONTINUE, the
 * program should continue to prompt the user.
 * 
 * @author Mauro Staver
 *
 */
public enum ShellStatus {

	CONTINUE, TERMINATE

}
